package com.kazemieh.www.batman.data.remote.model

import com.kazemieh.www.batman.domin.model.AllMoves
import com.kazemieh.www.batman.domin.model.Movie

data class MovieResponse(
    val imdbID: String? = null,
    val Actors: String? = null,
    val Awards: String? = null,
    val BoxOffice: String? = null,
    val Country: String? = null,
    val DVD: String? = null,
    val Director: String? = null,
    val Genre: String? = null,
    val Language: String? = null,
    val Metascore: String? = null,
    val Plot: String? = null,
    val Poster: String? = null,
    val Production: String? = null,
    val Rated: String? = null,
    val Ratings: List<RatingResponse>? = null,
    val Released: String? = null,
    val Response: String? = null,
    val Runtime: String? = null,
    val Title: String? = null,
    val Type: String? = null,
    val Website: String? = null,
    val Writer: String? = null,
    val Year: String? = null,
    val imdbRating: String? = null,
    val imdbVotes: String? = null
)

fun MovieResponse.toMovie() = Movie(
   imdbID= imdbID,
   Actors= Actors,
   Awards= Awards,
   BoxOffice= BoxOffice,
   Country= Country,
   DVD= DVD,
   Director= Director,
   Genre= Genre,
   Language= Language,
   Metascore= Metascore,
   Plot= Plot,
   Poster= Poster,
   Production= Production,
   Rated= Rated,
   Ratings= Ratings?.map { it.toRating() },
   Released= Released,
   Response= Response,
   Runtime= Runtime,
   Title= Title,
   Type= Type,
   Website= Website,
   Writer= Writer,
   Year= Year,
   imdbRating= imdbRating,
   imdbVotes= imdbVotes,
)